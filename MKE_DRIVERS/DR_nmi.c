/**
	\fn void InicializarNMI( void )
	\brief Inicializa el Handler de excepcion del NMI
 	\return void
*/
void InicializarNMI( void )
{
	//NMI deshabilitado, SWDDIO habilitado para debug
	SIM_SOPT = SIM_SOPT_RSTPE_MASK | SIM_SOPT_SWDE_MASK;
}
/**
	\fn void NMI_Handler(void)
	\brief Handler de excepcion del NMI
 	\details Anula la funcion de NMI
	\return void
*/
void NMI_Handler(void)
{
	SIM_SOPT = SIM_SOPT_RSTPE_MASK | SIM_SOPT_SWDE_MASK;
}
